-- Publication Readiness, Workstream 2 + foundation of Workstream 7/8.
--
-- publication_artifacts is the version-bound ledger of real, verified
-- publication evidence: an image, a PDF, a deployed webpage, a sent email,
-- a thank-you page, a form, or a record of an external post (LinkedIn/GBP).
-- It is ADDITIVE to, not a replacement for, the single-asset-per-version
-- columns already on deliverable_versions (storage_path, asset_mime,
-- asset_size_bytes, asset_name, asset_sha256, asset_validation, added by
-- 20260713185808_pdf_artifact_integrity.sql, already live in production).
-- Those columns remain the canonical binding for a deliverable's OWN
-- single lead-magnet PDF; publication_artifacts exists because a single
-- deliverable version can need SEVERAL artifacts at once (a Counsel Note
-- needs a hero image AND a deployed webpage; a GBP post needs a campaign
-- image AND a record of the actual external post), which the flat
-- deliverable_versions columns cannot represent.
--
-- Trigger and RLS pattern copied deliberately from the most recent
-- precedent in this schema, deliverable_suggestions
-- (production migration versions 20260713234759_deliverable_suggestions /
-- 20260714011950_deliverable_suggestion_release_hardening, tracked pending
-- rename on origin/feat/deliverable-suggestions-release): a
-- before-insert scope-validation trigger, a before-update-or-delete
-- unconditional-raise append-only trigger, RLS enabled + forced with zero
-- policies (service-role-only access, matching the Database Access
-- Invariant), SECURITY INVOKER on any RPC, explicit REVOKE ALL / GRANT
-- EXECUTE TO service_role, NOTIFY pgrst reload at the end.
--
-- superseded_at is deliberately NOT how staleness is determined. Staleness
-- is a DERIVED property computed by the readiness evaluator (Workstream 4)
-- by comparing an artifact's version_id against the deliverable's current
-- approved version. The column exists only for an administrative,
-- insert-time-only note (e.g. "this artifact was manually pulled down");
-- it is never updated on an existing row, and nothing in this system reads
-- it to decide readiness. DR-094 ("never overwrite an approved artifact,
-- preserve the prior file, hash and approval history when replacing it")
-- is honored by inserting a new row for a new version, never mutating an
-- old one.

create table if not exists public.publication_artifacts (
  id uuid primary key default gen_random_uuid(),
  firm_id uuid not null,
  deliverable_id uuid not null references public.content_deliverables(id) on delete restrict,
  version_id uuid not null references public.deliverable_versions(id) on delete restrict,
  artifact_type text not null check (artifact_type in (
    'hero_image', 'social_image', 'pdf', 'webpage', 'email',
    'thank_you_page', 'form', 'external_post'
  )),
  locale text,
  destination text check (
    destination is null or destination in ('firm_website', 'linkedin', 'google_business_profile')
  ),
  storage_bucket text,
  storage_path text,
  public_url text,
  repository text,
  repository_path text,
  deployment_commit text,
  deployment_url text,
  mime_type text,
  size_bytes integer,
  sha256 text,
  validation_result jsonb,
  created_by_role text not null check (created_by_role in ('operator', 'lawyer', 'system')),
  created_by_id uuid,
  created_at timestamptz not null default now(),
  superseded_at timestamptz,
  constraint publication_artifacts_locale_format_check
    check (locale is null or locale ~ '^[a-z]{2,3}-[A-Z]{2}$'),
  constraint publication_artifacts_sha256_format_check
    check (sha256 is null or sha256 ~ '^[0-9a-f]{64}$'),
  constraint publication_artifacts_validation_object_check
    check (validation_result is null or jsonb_typeof(validation_result) = 'object'),
  -- A durable storage-backed artifact never stores an expiring signed URL as
  -- its identity: public_url, when set for a storage-backed artifact, must
  -- be a stable public path. storage_bucket + storage_path are the durable
  -- identity; signed URLs are generated on demand at read time, never
  -- persisted here.
  constraint publication_artifacts_storage_pair_check
    check (
      (storage_bucket is null) = (storage_path is null)
    )
);

comment on table public.publication_artifacts is
  'Version-bound ledger of real publication evidence (images, PDFs, deployed webpages, emails, external posts). Never generated by this system; only registered against evidence that already exists.';
comment on column public.publication_artifacts.superseded_at is
  'Insert-time-only administrative note, never updated after insert. Readiness staleness is derived from version_id vs. the deliverable''s current approved version, not from this column.';

create index if not exists publication_artifacts_deliverable_idx
  on public.publication_artifacts (deliverable_id, version_id, artifact_type);
create index if not exists publication_artifacts_firm_idx
  on public.publication_artifacts (firm_id, created_at desc);
create index if not exists publication_artifacts_type_idx
  on public.publication_artifacts (artifact_type, locale);

create or replace function public.validate_publication_artifact_scope()
returns trigger
language plpgsql
set search_path = public
as $function$
declare
  v_deliverable_firm uuid;
  v_version_deliverable uuid;
  v_version_firm uuid;
begin
  select d.firm_id, v.deliverable_id, v.firm_id
    into v_deliverable_firm, v_version_deliverable, v_version_firm
  from public.content_deliverables d
  join public.deliverable_versions v on v.deliverable_id = d.id
  where d.id = new.deliverable_id and v.id = new.version_id;

  if not found
     or v_deliverable_firm is distinct from new.firm_id
     or v_version_deliverable is distinct from new.deliverable_id
     or v_version_firm is distinct from new.firm_id then
    raise exception 'publication artifact must reference a version from the same firm and deliverable';
  end if;
  return new;
end;
$function$;

drop trigger if exists trg_validate_publication_artifact_scope on public.publication_artifacts;
create trigger trg_validate_publication_artifact_scope
before insert on public.publication_artifacts
for each row execute function public.validate_publication_artifact_scope();

