create or replace function public.block_publication_release_identity_mutation()
returns trigger
language plpgsql
set search_path = public
as $function$
begin
  if tg_op = 'DELETE' then
    raise exception 'publication releases cannot be deleted';
  end if;
  if new.firm_id is distinct from old.firm_id
     or new.period_id is distinct from old.period_id
     or new.created_at is distinct from old.created_at
     or new.id is distinct from old.id then
    raise exception 'publication release identity fields (firm_id, period_id, created_at, id) cannot change; status, manifest_snapshot, and authorization fields may';
  end if;
  return new;
end;
$function$;

drop trigger if exists trg_block_publication_release_identity_mutation on public.publication_releases;
create trigger trg_block_publication_release_identity_mutation
before update or delete on public.publication_releases
for each row execute function public.block_publication_release_identity_mutation();

