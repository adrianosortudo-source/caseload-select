-- ---------------------------------------------------------------------------
-- publication_artifact_validations (Workstream 7 foundation): append-only
-- record of a reconciliation check run against a registered artifact
-- (storage object exists + hash/size/mime match, route responds, deployment
-- evidence resolves). Kept separate from publication_artifacts itself so
-- validation history stays auditable across repeated reconciliation runs
-- without ever mutating the artifact row it validates.
-- ---------------------------------------------------------------------------

create table if not exists public.publication_artifact_validations (
  id uuid primary key default gen_random_uuid(),
  artifact_id uuid not null references public.publication_artifacts(id) on delete restrict,
  firm_id uuid not null,
  validator text not null check (validator in (
    'storage_object_check', 'sha256_check', 'route_check', 'deployment_check'
  )),
  result text not null check (result in ('pass', 'fail', 'error')),
  details jsonb,
  validated_by_role text not null check (validated_by_role in ('operator', 'system')),
  validated_by_id uuid,
  created_at timestamptz not null default now(),
  constraint publication_artifact_validations_details_object_check
    check (details is null or jsonb_typeof(details) = 'object')
);

comment on table public.publication_artifact_validations is
  'Append-only reconciliation history for a publication artifact. Never mutates the artifact it validates.';

create index if not exists publication_artifact_validations_artifact_idx
  on public.publication_artifact_validations (artifact_id, created_at desc);
create index if not exists publication_artifact_validations_firm_idx
  on public.publication_artifact_validations (firm_id, created_at desc);

