-- ---------------------------------------------------------------------------
-- Workstream 8 foundation: publication_releases / publication_release_items.
-- Data foundation only. No publish connectors, no UI authorize action in
-- this phase. publication_releases carries a mutable status (like
-- content_deliverables.status) because a release genuinely has a
-- lifecycle; its IDENTITY fields (firm_id, period_id, created_at) are
-- protected from mutation by trigger. publication_release_items is fully
-- immutable once inserted, matching "adding a newer deliverable version
-- must not rewrite an existing release snapshot": each item freezes the
-- exact version and artifact set it was created against.
-- ---------------------------------------------------------------------------

create table if not exists public.publication_releases (
  id uuid primary key default gen_random_uuid(),
  firm_id uuid not null,
  period_id uuid not null references public.content_periods(id) on delete restrict,
  status text not null default 'draft' check (status in (
    'draft', 'blocked', 'ready', 'authorized', 'publishing', 'published', 'failed', 'rolled_back'
  )),
  manifest_snapshot jsonb not null default '{}'::jsonb,
  authorized_by_role text check (authorized_by_role is null or authorized_by_role in ('operator', 'lawyer')),
  authorized_by_id uuid,
  authorized_at timestamptz,
  created_at timestamptz not null default now(),
  constraint publication_releases_manifest_object_check
    check (jsonb_typeof(manifest_snapshot) = 'object'),
  constraint publication_releases_authorization_pair_check
    check ((authorized_at is null) = (authorized_by_role is null))
);

comment on table public.publication_releases is
  'A named, period-scoped release candidate. No publish action exists yet in this phase; this is the data foundation only.';

create index if not exists publication_releases_period_idx
  on public.publication_releases (period_id, created_at desc);
create index if not exists publication_releases_firm_status_idx
  on public.publication_releases (firm_id, status);

create table if not exists public.publication_release_items (
  id uuid primary key default gen_random_uuid(),
  release_id uuid not null references public.publication_releases(id) on delete restrict,
  firm_id uuid not null,
  deliverable_id uuid not null references public.content_deliverables(id) on delete restrict,
  version_id uuid not null references public.deliverable_versions(id) on delete restrict,
  artifact_snapshot jsonb not null default '[]'::jsonb,
  readiness_snapshot jsonb not null,
  included boolean not null default true,
  exclusion_reason text,
  created_at timestamptz not null default now(),
  constraint publication_release_items_artifact_array_check
    check (jsonb_typeof(artifact_snapshot) = 'array'),
  constraint publication_release_items_readiness_object_check
    check (jsonb_typeof(readiness_snapshot) = 'object'),
  constraint publication_release_items_exclusion_pair_check
    check (included or exclusion_reason is not null)
);

comment on table public.publication_release_items is
  'Immutable per-deliverable snapshot inside a release: the exact version and artifact set as of release creation. Never rewritten by a later deliverable version.';

create index if not exists publication_release_items_release_idx
  on public.publication_release_items (release_id);
create index if not exists publication_release_items_deliverable_idx
  on public.publication_release_items (deliverable_id, version_id);

create or replace function public.validate_publication_release_item_scope()
returns trigger
language plpgsql
set search_path = public
as $function$
declare
  v_release_firm uuid;
  v_deliverable_firm uuid;
  v_version_deliverable uuid;
  v_version_firm uuid;
begin
  select firm_id into v_release_firm
    from public.publication_releases
   where id = new.release_id;
  if not found or v_release_firm is distinct from new.firm_id then
    raise exception 'release item must reference a release from the same firm';
  end if;

  select d.firm_id, v.deliverable_id, v.firm_id
    into v_deliverable_firm, v_version_deliverable, v_version_firm
  from public.content_deliverables d
  join public.deliverable_versions v on v.deliverable_id = d.id
  where d.id = new.deliverable_id and v.id = new.version_id;

  if not found
     or v_deliverable_firm is distinct from new.firm_id
     or v_version_deliverable is distinct from new.deliverable_id
     or v_version_firm is distinct from new.firm_id then
    raise exception 'release item must reference a version from the same firm and deliverable';
  end if;
  return new;
end;
$function$;

