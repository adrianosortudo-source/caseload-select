const ADAPTERS: readonly PublicationAdapter[] = [websiteAdapter, linkedInAdapter, gbpAdapter, emailAdapter];

export function getPublicationAdapter(destination: PlacementDestination): PublicationAdapter {
  const adapter = ADAPTERS.find((a) => a.destinations.includes(destination));
  if (!adapter) throw new Error(`no publication adapter registered for destination: ${destination}`);
  return adapter;
}

