/**
 * intake-memo.ts  -  Shared memo + follow-up presentation helpers.
 *
 * Single source of truth for what the lawyer sees after intake completes,
 * across three surfaces:
 *   - Demo overlay (src/components/demo/LawyerViewPanel.tsx)
 *   - Admin CRM lead detail (src/app/leads/[id]/page.tsx)
 *   - Firm portal lead detail (src/app/portal/[firmId]/leads/[leadId]/page.tsx)
 *
 * Two things live here:
 *
 *   1. BAND_FOLLOWUP  -  numbered next-steps per CPI band. Pure presentation
 *      copy, identical across all surfaces.
 *
 *   2. buildDynamicMemo  -  fallback memo builder for the demo overlay, used
 *      when no scenario fixture is selected and no AI-generated memo is
 *      available. The real product consumes intake_sessions.memo_text
 *      directly (generated by src/lib/memo.ts after Round 3).
 *
 * No "use client" directive: this module exports only plain data and pure
 * functions (no hooks, no browser APIs), so it's safely importable from
 * both server components (admin/portal pages) and client components
 * (the demo overlay).
 */

