// Demo overlay fallback. When the demo has no scenarioId fixture and no AI
// memo_text is available, produce a memo from the raw session shape so every
// A/B/C case still renders something coherent. Real product does NOT use this:
// it reads intake_sessions.memo_text directly.
export function buildDynamicMemo(params: {
  situationSummary: string | null;
  practiceArea: string | null;
  intakeTrail?: Array<{ question: string; answer: string }>;
}): MemoData {
  const { situationSummary, practiceArea, intakeTrail } = params;
  const paKey = practiceArea?.toLowerCase() ?? "";
  const gaps = PRACTICE_AREA_GAPS[paKey] ?? DEFAULT_GAPS;

  const trailItems = (intakeTrail ?? []).slice(0, 6).map(item => {
    const q = item.question.replace(/[?:.]+\s*$/, "").trim();
    const shortQ = q.length > 48 ? q.slice(0, 45) + "..." : q;
    const shortA = item.answer.length > 90 ? item.answer.slice(0, 87) + "..." : item.answer;
    return { checked: true, text: `${shortQ}: ${shortA}` };
  });

  const evidenceItems: MemoData["evidenceItems"] = [];
  if (situationSummary) {
    const short = situationSummary.length > 200
      ? situationSummary.slice(0, 197) + "..."
      : situationSummary;
    evidenceItems.push({ checked: true, text: `Client account: ${short}` });
  }
  evidenceItems.push(...trailItems);

  return {
    jurisdictionTimeline: "Ontario. Exact dates and limitation period to be confirmed with client on initial call.",
    evidenceHeading: "Intake Summary",
    evidenceItems,
    adverseParties: "To be confirmed with client on initial call. Conflict check pending.",
    gaps,
  };
}
