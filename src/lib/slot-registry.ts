/**
 * CaseLoad Screen  -  Slot Registry
 *
 * Deterministic qualification question banks, one bank per practice area sub-type.
 * Slots are the structured fact-collection layer  -  they tell us what we know about
 * a case in machine-readable form and produce deterministic score deltas.
 *
 * Architecture:
 *   - Gate questions (flag-registry.ts): compliance/malpractice gates, S1 priority
 *   - Slots (this file): qualification depth, case valuation, three-axis scoring
 *
 * Slot scoring deltas (fitDelta, urgencyDelta, frictionDelta) are lawyer-authored
 * judgments encoded as data. They adjust the three CPI axes (0-100 normalized scale)
 * when a slot is answered. Never generated by GPT.
 *
 * Selection rules:
 *   Round 1  -  deterministic lookup, top-N by priorityWeight, no GPT.
 *   Round 2  -  GPT selects from filtered candidates (dependency-satisfied, unanswered).
 *   Round 3  -  fires only for Band A/B leads when any option has triggersRound3: true.
 *
 * Integration point: src/lib/slot-selector.ts  -  selectSlots(), scoreFromSlotAnswers()
 * Called from: src/app/api/screen/route.ts  -  after classifier resolves sub_type.
 *
 * Universal slots (UNIVERSAL_SLOTS):
 *   A small set of sub-type-agnostic slots served in every session where a slot
 *   bank is active. Currently: court_centre (KB-17 routing gate).
 *   Universal slots always appear before sub-type slots within a round and are
 *   excluded from the per-round sub-type cap  -  they consume cap budget but are
 *   never cut. selectSlots() handles the separation.
 *
 * Build order: seed one sub-type, prove the pipeline, expand bank-by-bank.
 * Current banks: pi_slip_fall | universal | crim_indictable_sc | civ_contract_dispute | fam_separation | div_tribunal_review | pi_mva | emp_dismissal
 */

// ─────────────────────────────────────────────────────────────────────────────
// Types
// ─────────────────────────────────────────────────────────────────────────────

export interface SlotOption {
  /** Machine-readable value stored in extracted_entities. */
  value: string;
  /** Display text shown to the client. */
  label: string;
  /**
   * Score deltas applied to the three CPI axes when this option is selected.
   * Units: normalized 0-100 scale (same as cpi_fit, cpi_urgency, cpi_friction).
   * Positive fitDelta = better case fit. Positive urgencyDelta = more time pressure.
   * Positive frictionDelta = more risk/concern. Deltas are ADDITIVE across slots.
   */
  fitDelta: number;
  urgencyDelta: number;
  frictionDelta: number;
  /** When true, triggers Round 3 questions regardless of current band score. */
  triggersRound3?: boolean;
  /**
   * When true, this answer disqualifies the lead (Band E routing).
   * Use only for hard jurisdictional/eligibility filters (e.g., occurred outside Ontario).
   */
  disqualifies?: boolean;
  /**
   * Inline follow-up question rendered immediately after this option is selected.
   * The follow-up answer is stored under followUp.id and is required before the
   * parent slot batch can be submitted.
   */
  followUp?: {
    id: string;
    text: string;
    description?: string;
    options?: Array<{ label: string; value: string; fitDelta: number; urgencyDelta: number; frictionDelta: number }>;
    allow_free_text?: boolean;
  };
}

export interface SlotDependency {
  /** The slot that must have been answered. */
  slotId: string;
  /** The answer value(s) that satisfy the dependency. */
  values: string[];
}

export interface Slot {
  /** Stable ID. Format: {subType}__{field}. E.g. "pi_slip_fall__incident_date". */
  id: string;
  /** Sub-type this slot belongs to. Matches practice_sub_type from screen-prompt. */
  subType: string;
  /** 1 = always served in round 1; 2 = served in round 2; 3 = high-value round 3. */
  round: 1 | 2 | 3;
  /** Question text shown to the client. Short, factual, no legal conclusions. */
  question: string;
  /** Optional one-sentence intro before the question. Never generated. */
  preamble?: string;
  /**
   * One-sentence context shown as grey subtext beneath the question label in the widget.
   * Used for sensitive questions (DV history, financial disclosure, prior injuries, capacity)
   * to explain why we are asking before the client answers. Never generated  -  always authored.
   */
  description?: string;
  /** Answer format. single_select and yes_no require options[]. */
  answerType: "single_select" | "multi_select" | "yes_no" | "numeric" | "text" | "date" | "file";
  /** Required for single_select, multi_select, yes_no. */
  options?: SlotOption[];
  /**
   * Higher weight = asked earlier in the round.
   * Round 1 serves the top 6 slots by weight. Round 2 serves top 5 from filtered set.
   */
  priorityWeight: number;
  /**
   * Conditional display: only show this slot when dependsOn.slotId has been answered
   * with one of dependsOn.values. Omit for unconditional slots.
   */
  dependsOn?: SlotDependency;
  /**
   * Server-side exclusion gate against confirmed R1/R2 AI answers (_confirmed).
   * Key: AI question ID (e.g. "pi_q17"). Value: answer values that suppress this slot.
   * Applied in slot-selector.ts before slots are injected into the GPT prompt.
   * Prevents treatment/income questions from being served when the client confirmed
   * no injuries in a prior AI turn.
   */
  excludeWhen?: Record<string, string[]>;
  /**
   * Optional widget rendering hint. Drives the IntakeWidget v2 layout choice.
   *   "card"   - decision card, 1 slot per screen, 2-column option grid
   *   "chip"   - inline chip pills, 3-4 slots grouped on one rapid-fire screen
   *   "slider" - bucketed slider for ordinal/ranged options
   * When omitted, the widget auto-resolves: yes_no | single_select with <=3 options -> chip;
   * single_select with >=4 options or multi_select -> card; numeric -> slider.
   */
  presentation?: "card" | "chip" | "slider";
}

